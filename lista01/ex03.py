# 3. Média de Três Números
# Crie um programa que receba três números do usuário e calcule a média aritmética deles.
# A fórmula é:

num01 = float(input('Digite o primeiro número:'))
num02 = float(input('Digite o segundo número:'))
num03 = float(input('Digite o terceiro número:'))

media = (num01+num02+num03)/3

print(f'Média:{media}')